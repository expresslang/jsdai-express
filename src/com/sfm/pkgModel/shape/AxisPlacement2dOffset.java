package com.sfm.pkgModel.shape;

public class AxisPlacement2dOffset
{
	double xOffset,yOffset;
	
	public AxisPlacement2dOffset(double x, double y)
	{
		xOffset = x;
		yOffset = y;
	}
	
	public double getXoffset()
	{
		return xOffset;
	}
	
	public double getYoffset()
	{
		return yOffset;
	}

}
